package fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		// quando se declara uma "var" é necessário inicializar a variavel
		var b = 4.5;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		c = "Recebe outro texto";
		System.out.println(c);
		
	}
}
